// After if statement we use else-if.
// We can use multiple else-if statements.
let marks = 100;

if (marks >= 80) {
    console.log("A+")
} else if (marks >= 60) {
    console.log("A")
} else if (marks >= 33) {
    console.log("B")
} else if (marks < 33) {
    console.log("F")
}
// Agr if statement mein true aa gya toh woh niche kitne bhi else-if likhe ho woh check ni krega.