let b = 5;

//Arithmetic Operator
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);

//Unary Operator
console.log(a++)    //Ishke baad a ki value 11 ho jayegi
