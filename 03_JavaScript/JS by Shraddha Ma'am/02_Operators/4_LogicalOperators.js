// It is used to combine expressions

// Logical AND(&&) - ALl conditions should be true then only final answer will be true otherwise false.
if((5 > 3) && (3 > 10)){    // false
    console.log(true);  
}

// Logical OR(||) - In all conditions, if one condition become true then final answer will be true.
if((5 > 3) || (3 > 10)){    // true
    console.log(true);
}

// Logical NOT(!) - Opposite of the original condition answer.
if(!(3 > 1)) {  // false
    console.log(true);
}