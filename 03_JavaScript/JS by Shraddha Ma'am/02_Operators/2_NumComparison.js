// Comparison Operators to compare 2 values.
let age = 18;
console.log(age < 18)
console.log(age <= 18)
console.log(age > 18)
console.log(age >= 18)

// We can use absolute values also
console.log(5 == 5)
console.log(5 != 5)
console.log(5 != 10)

// Comparing with strings

// == : compares value, not type 
console.log(5 == '5')
console.log(0 == " ")
// === : compares type and value
console.log(123 === "123")
console.log(0 === " ")